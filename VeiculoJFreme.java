    private void jButtonAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAtualizarActionPerformed
        // TODO add your handling code here:
        
       try {
           Veiculo v = new Veiculo();
           DadosVeiculo dados =  new DadosVeiculo();
            NegocioVeiculo negocio = new NegocioVeiculo();
           
            v.setPlaca(jTextPlaca.getText());
            v.setTipo_auto(jTextTipoAuto.getText());
            v.setMarca(jTextMarca.getText());
            v.setModelo(jTextModelo.getText());
            v.setId((int)jTableVeiculo.getValueAt(jTableVeiculo.getSelectedRow(),0));
            dados.atualizarVeiculo(v);
            negocio.atualizarVeiculo(v);
            
            
            jTextPlaca.setText("");
            jTextTipoAuto.setText("");
            jTextMarca.setText("");
            jTextModelo.setText("");
            
            JOptionPane.showMessageDialog(this, "Atualizado com sucesso");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        
        
    }//GEN-LAST:event_jButtonAtualizarActionPerformed

    private void jButtonRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRemoverActionPerformed
        // TODO add your handling code here:
        
         try {
             
              if(jTableVeiculo.getSelectedRow() != -1){
             
           Veiculo v = new Veiculo();
           DadosVeiculo dados =  new DadosVeiculo();
           
          
          
            v.setId((int)jTableVeiculo.getValueAt(jTableVeiculo.getSelectedRow(),0));
            
            dados.removerVeiculo(v);
            
            
            
              jTextPlaca.setText("");
              jTextTipoAuto.setText("");
              jTextMarca.setText("");
            jTextModelo.setText("");
            
            
            
            
            JOptionPane.showMessageDialog(this, "Veiculo removido");
             }          
        } catch (Exception e) {
             JOptionPane.showMessageDialog(this, "Selecione  um veiculo para remover");
        }
            
        
        
    }//GEN-LAST:event_jButtonRemoverActionPerformed

