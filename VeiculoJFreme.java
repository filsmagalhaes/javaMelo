    private void jButtonCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadastrarActionPerformed
        // TODO add your handling code here:
        try {
           Veiculo v = new Veiculo();
            if (jTextPlaca.getText().trim().equals("") == true) {
                JOptionPane.showMessageDialog(this, "Informe a placa");
                return;
            }
            v.setPlaca(jTextPlaca.getText());
            v.setTipo_auto(jTextTipoAuto.getText());
            v.setMarca(jTextMarca.getText());
            v.setModelo(jTextModelo.getText());
            NegocioVeiculo dados = new NegocioVeiculo();
            dados.cadastrarVeiculo(v);
            
              jTextPlaca.setText("");
            jTextTipoAuto.setText("");
            jTextMarca.setText("");
            jTextModelo.setText("");
            
            JOptionPane.showMessageDialog(this, "Veiculo cadastrado!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
           
    }//GEN-LAST:event_jButtonCadastrarActionPerformed

    private void jButtonListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonListarActionPerformed
        // TODO add your handling code here:
         try {
            Veiculo filtro = new Veiculo();
            if (jTextPlaca.getText().trim().matches("") == false) {
                filtro.setPlaca(jTextPlaca.getText());
            }
            
            filtro.setTipo_auto(jTextTipoAuto.getText());
            DadosVeiculo dados = new DadosVeiculo();
            ArrayList<Veiculo> lista = dados.listar(filtro);
            DefaultTableModel modelo = new DefaultTableModel();
            modelo.setColumnIdentifiers(new Object[]{"ID","placa","tipo_auto","marca","modelo","data_cadastro"});
            for (Veiculo v : lista){
                modelo.addRow(new Object[] {v.getId(), v.getPlaca(), v.getTipo_auto(), v.getMarca(), v.getModelo(), v.getData_cadastro()});
            }
            jTableVeiculo.setModel(modelo);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_jButtonListarActionPerformed

