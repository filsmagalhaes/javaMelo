    private void carregarComboAlunos() {
        try {
            Aluno filtro = new Aluno();
            DadosAluno dados = new DadosAluno();
            this.listaAlunos = dados.listar(filtro);
            DefaultComboBoxModel modelo = new DefaultComboBoxModel();
            for (Aluno aluno : this.listaAlunos) {
                modelo.addElement(aluno.getNome());
            }
            jComboBoxAlunos.setModel(modelo);
            //JOptionPane.showMessageDialog(this, "Aluno cadastrado");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }

