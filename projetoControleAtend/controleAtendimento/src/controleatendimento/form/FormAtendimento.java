    private void jButtonCadastrarActionPerformed(java.awt.event.ActionEvent evt) {                                                 
        // TODO add your handling code here:
        try {
            Avaliacao avaliacao = new Avaliacao();
            avaliacao.setNota(Float.parseFloat(jTextFieldNota.getText()));
            avaliacao.setObservacao(jTextFieldObs.getText());
            int index = jComboBoxAlunos.getSelectedIndex();
            Aluno a = this.listaAlunos.get(index);
            avaliacao.setAluno(a);
            DadosAvaliacao dados = new DadosAvaliacao();
            dados.cadastrarAvaliacao(avaliacao);
            JOptionPane.showMessageDialog(this, "Avaliação Cadastrada");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }     
