    private void jButtonCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadastrarActionPerformed
        // TODO add your handling code here:
        try {
            Terapeuta t = new Terapeuta();
            if (jTextFieldCpf.getText().matches("[0-9]+") == false) {
                JOptionPane.showMessageDialog(this, "Informe apenas nÃºmeros para a matricula");
                return;
            }else if(jTextFieldAgencia.getText().matches("[0-9]+") == false) {
                JOptionPane.showMessageDialog(this, "Informe apenas nÃºmeros para a agÃªncia");
                return;
            }else if(jTextFieldConta.getText().matches("[0-9]+") == false) {
                JOptionPane.showMessageDialog(this, "Informe apenas nÃºmeros para a conta");
                return;
            }
            t.setCpf(jTextFieldCpf.getText());
            t.setNome(jTextFieldNome.getText());
            t.setEspecialidade(jTextFieldEspecialidade.getText());
            t.setTelefone(jTextFieldFone.getText());
            t.setEmail(jTextFieldMail.getText());
            t.setBanco(jTextFieldBanco.getText());
            t.setAgencia(Integer.parseInt(jTextFieldAgencia.getText()));
            t.setConta(Integer.parseInt(jTextFieldConta.getText()));
            
            NegocioTerapeuta dados = new NegocioTerapeuta();
            dados.cadastrarTerapeuta(t);
            JOptionPane.showMessageDialog(this, "Terapeuta cadastrado(a)");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_jButtonCadastrarActionPerformed

    private void jButtonListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonListarActionPerformed
        // TODO add your handling code here:
        try {
            Terapeuta filtro = new Terapeuta();
            if (jTextFieldCpf.getText().matches("[0-9]+") == true) {
                filtro.setCpf(jTextFieldCpf.getText());
            }

            filtro.setNome(jTextFieldNome.getText());
            DadosTerapeuta dados = new DadosTerapeuta();
            ArrayList<Terapeuta> lista = dados.listar(filtro);
            DefaultTableModel modelo = new DefaultTableModel();
            modelo.setColumnIdentifiers(new Object[]{"Nome","Especialidade"});
            for (Terapeuta terapeuta : lista) {
                modelo.addRow(new Object[]{terapeuta.getNome(),terapeuta.getEspecialidade()});
            }
            jTableTerapeuta.setModel(modelo);
            //JOptionPane.showMessageDialog(this, "Aluno cadastrado");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_jButtonListarActionPerformed

